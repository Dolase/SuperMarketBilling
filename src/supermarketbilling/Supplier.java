    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try
        {
            if(pid>0)
            {
                if(sname.getText().equals("") || cname.getText().equals("") || sstatus.getSelectedItem().equals("") || address.getText().equals(""))
                {
                    jOptionPane1.showMessageDialog(null,"All field required !");
                }
                else
                {
                    Connect c=new Connect();
                    Connection con=c.getConnection();
                    Statement st=con.createStatement();
                    int ch=st.executeUpdate("update supplier set supplier_name='"+sname.getText()+"', compony_name='"+cname.getText()+"',company_address='"+address.getText()+"', status='"+sstatus.getSelectedItem()+"' where supplier_id="+pid+" ");
                    if(ch>0)
                    {
                        jOptionPane1.showMessageDialog(null,"supplier update successfully");
                    }
                    else
                    {
                        jOptionPane1.showMessageDialog(null,"unable to updatesupplier");
                    }
                    con.close();
                }

            }
            else
            {

                jOptionPane1.showMessageDialog(null,"no supplier select for update");
            }
        }
        catch(Exception ex)
        {
            jOptionPane1.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

