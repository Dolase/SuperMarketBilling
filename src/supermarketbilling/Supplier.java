    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            if(search.getText().equals(""))
            {
                jOptionPane1.showMessageDialog(null,"please enter product ID");
            }
            else
            {
                Connect c=new Connect();
                Connection con=c.getConnection();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select * from supplier where supplier_id="+search.getText()+"");
                if(rs.next())
                {
                     pid=rs.getInt(1);
                    sname.setText(rs.getString(2));
                    cname.setText(rs.getString(3));
                    address.setText(rs.getString(4));
   
                    if(rs.getString(4).equals("Active"))
                    {
                        sstatus.setSelectedIndex(0);
                    }
                    else
                    {
                        sstatus.setSelectedIndex(1);
                    }
                }
                else
                {
                    clear_data();
                    pid=0;
                    jOptionPane1.showMessageDialog(null,"record not found");
                }
                con.close();
            }
        }
        catch(Exception ex)
        {
            jOptionPane1.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

