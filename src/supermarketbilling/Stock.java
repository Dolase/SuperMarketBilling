    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        try
        {
            
            if(sname.getSelectedItem().equals("") || pname.getSelectedItem().equals("") || pprice.getText().equals("") || punit.getText().equals(""))
            {
                jOptionPane1.showMessageDialog(null,"All field required !");
            }
            else
            {
                Date date = new Date();  
                SimpleDateFormat dt = new SimpleDateFormat("dd/MM/yyyy");  
                String d= dt.format(date);  
                Connect c=new Connect();
                Connection con=c.getConnection();
                Statement st=con.createStatement();
                int ch=st.executeUpdate("insert into stock(product_id, supplier_id, stock_date, stock_purchase_price, stock_unit) values('"+pname.getSelectedItem().toString().charAt(0)+"','"+sname.getSelectedItem().toString().charAt(0)+"','"+d+"','"+pprice.getText()+"','"+punit.getText()+"')");
                if(ch>0)
                {
                    jOptionPane1.showMessageDialog(null,"Stock add successfully !");
                    clear_data();
                }
                else
                {
                    jOptionPane1.showMessageDialog(null,"Unable to add new Stock !");
                }
                con.close();
            }
        }
        catch(Exception ex)
        {
            jOptionPane1.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

