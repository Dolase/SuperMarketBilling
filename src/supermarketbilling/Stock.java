    public void load_data()
    {
        try
        {
            Connect c=new Connect();
                Connection con=c.getConnection();
                Statement st=con.createStatement();
                 Statement st1=con.createStatement();
                ResultSet rs=st.executeQuery("select * from supplier");
                while(rs.next())
                {
                    sname.addItem(rs.getString(1)+" "+rs.getString(2));
                }
                rs=st.executeQuery("select * from product");
                 while(rs.next())
                {
                    pname.addItem(rs.getString(1)+" "+rs.getString(2));
                }
                 
                 con.close();
            
        }
        catch(Exception ex)
        {
            jOptionPane1.showMessageDialog(null,ex); 
        }
    }
    
    public void set_data(int cpid,int csid )
    {
        try
        {
            sname.removeAllItems();
            pname.removeAllItems();
                Connect c=new Connect();
                Connection con=c.getConnection();
                Statement st=con.createStatement();
                 Statement st1=con.createStatement();
                ResultSet rs=st.executeQuery("select * from supplier where supplier_id="+csid+"");
                while(rs.next())
                {
                    sname.addItem(rs.getString(1)+" "+rs.getString(2));
                }
                rs=st.executeQuery("select * from product where product_id="+cpid+"");
                 while(rs.next())
                {
                    pname.addItem(rs.getString(1)+" "+rs.getString(2));
                }
                 con.close();
            
        }
        catch(Exception ex)
        {
            jOptionPane1.showMessageDialog(null,ex); 
        }
    }

